<script setup>
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        required: true,
    },
    value: {
        default: null,
    },
});

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <input
        type="checkbox"
        :value="value"
        v-model="proxyChecked"
        class="
            rounded-md appearance-none w-4 h-4
            border-2
            border-gray-600
            outline
            outline-transparent
            transition-all
            focus:outline-offset-2
            checked:border-transparent
            focus:outline-lime-600
            checked:bg-lime-600
            "
    />
</template>

